# Tupel oder Tuple

# Erstellen: wie Listen, nur mit runden Klammern
some_tuple = (1, 2, "abc")

# Ausgabe: ähnlich wie Listen
print(some_tuple)

# einen Wert erhalten: wie bei Listen
print(some_tuple[2])

some_tuple = 7

